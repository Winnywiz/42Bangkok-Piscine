int	ft_strcmp(char *s1, char *s2)
{
	unsigned char	*_s1;
	unsigned char	*_s2;

	_s1 = (unsigned char *) s1;
	_s2 = (unsigned char *) s2;
	while (*_s1 != '\0' || *_s2 != '\0')
	{
		if (*_s1 != *_s2)
		{
			break ;
		}
		_s1++;
		_s2++;
	}
	return (*_s1 - *_s2);
}
