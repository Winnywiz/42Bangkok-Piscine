char	*ft_strlowcase(char *str)
{
	char	*proc;

	proc = str;
	while (*proc != '\0')
	{
		if (*proc >= 'A' && *proc <= 'Z')
		{
			*proc += 32;
		}
		proc++;
	}
	return (str);
}
