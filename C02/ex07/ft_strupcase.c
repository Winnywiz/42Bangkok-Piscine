char	*ft_strupcase(char *str)
{
	char	*proc;

	proc = str;
	while (*proc != '\0')
	{
		if (*proc >= 'a' && *proc <= 'z')
		{
			*proc -= 32;
		}
		proc++;
	}
	return (str);
}
