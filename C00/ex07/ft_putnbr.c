#include <unistd.h>

void	ft_putnbr(int nb);

void	ft_putnbr(int nb)
{
	char	disp;

	if (nb == -2147483648)
	{
		write(1, "-", 1);
		write(1, "2147483648", 12);
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		nb %= 10;
	}
	disp = nb + 48;
	write(1, &disp, 1);
}
